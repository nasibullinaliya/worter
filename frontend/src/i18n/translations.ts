export type TKey = keyof typeof T['ru']

export function getT(lang: Lang) {
  return (key: TKey): string => T[lang][key] ?? T['ru'][key] ?? key
}

export function getWordLabel(lang: Lang, n: number): string {
  if (lang === 'ru') {
    if (n % 10 === 1 && n % 100 !== 11) return 'слово'
    if ([2, 3, 4].includes(n % 10) && ![12, 13, 14].includes(n % 100)) return 'слова'
    return 'слов'
  }
  if (lang === 'de') return n === 1 ? 'Wort' : 'Wörter'
  return n === 1 ? 'word' : 'words'
}

export function getSetLabel(lang: Lang, n: number): string {
  if (lang === 'ru') {
    if (n % 10 === 1 && n % 100 !== 11) return 'набор'
    if ([2, 3, 4].includes(n % 10) && ![12, 13, 14].includes(n % 100)) return 'набора'
    return 'наборов'
  }
  if (lang === 'de') return n === 1 ? 'Set' : 'Sets'
  return n === 1 ? 'set' : 'sets'
}

export function getDateLocale(lang: Lang): string {
  if (lang === 'de') return 'de-DE'
  if (lang === 'en') return 'en-US'
  return 'ru-RU'
}
